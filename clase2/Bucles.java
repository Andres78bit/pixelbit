public class Bucles {
    public static void main(String[] args) {

        // Bucle for: inicio; condición de parada; incremento o decremento
        for (int i = 1; i <= 5; i++) {
            System.out.println("Número de Iteración: " + i);
        }

        /*
         * Estructura del while
         * Inicio
         * while (condición) {
         * // Código a ejecutar
         * }
         */

        int j = 0;
        while (j <= 5) {
            System.out.println("Número de Iteración: " + j);
            j++;
        }
    }
}